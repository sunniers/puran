package com.shdatabank.puruan.model;

/**
 * 查询下拉列表model
 */
public class ListModel {
	
	/** 字典ID */
	private int id;
	/** 字典值 */
	private String value;
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getValue() {
		return value;
	}
	public void setValue(String value) {
		this.value = value;
	}

}
